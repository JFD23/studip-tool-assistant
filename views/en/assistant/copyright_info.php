    <p>The most important basic rule: a work belongs to the people who created it. They determine how the work may be used.
    </p>
    <p>
        If you want to use or pass on to your students something that you have not created yourself, you must always check whether you are allowed to do so.
    </p>
    <p>
        There are some common misunderstandings:
    </p>
    <ul>
        <li>You may not copy and distribute things that you find on the Internet just because they are freely accessible. Check whether a license is indicated on the website that allows you to pass on the material. If not, just link to the website.</li>
        <li>Works created by students, including term papers, theses, etc. belong solely to the students and may not be used without their explicit consent.</li>
        <li>Even in the closed room of a course you are not allowed to pass on copyrighted works without further investigation. You can read more about this in the section "What may I use in teaching?".</li>
    </ul>

